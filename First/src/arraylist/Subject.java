package arraylist;

// Subject 클래스 구현하기
public class Subject {
	// Subject 클래스 멤버변수
	private String name;
	private int scorePoint;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	public int getScorePoint() {
		return scorePoint;
	}
	
	public void setScorePoint(int scorePoint) {
		this.scorePoint = scorePoint;
	}

}
